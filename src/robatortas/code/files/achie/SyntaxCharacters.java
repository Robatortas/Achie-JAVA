package robatortas.code.files.achie;

/** <NEWLINE>
 * <b>SyntaxCharacters interface</b>
 * <br><br>
 * Contains all the characters the syntax needs to function!
 */
public interface SyntaxCharacters {
	final char START_VARIABLE= '<';
	final char END_VARIABLE = '>';
	final char START_CURLY_BRACE = '{';
	final char END_CURLY_BRACE = '}';
	final char START_VALUE = ':';
	final char END_VALUE= ';';
	final char COMMENT = '#';
}